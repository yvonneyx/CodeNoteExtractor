/**
 * <zh/> 两个数之和
 * 
 * <en/> Sum of two numbers
 * @remarks
 * This is a remark for add function to explain more about it.
 * @param a the first num
 * @param b the second num
 * @returns Sum of two numbers
 * @example
 * ```ts
 * import { add } from 'my-ts-lib';
 * const sum = add(1, 2); // 3
 * ```
 */
export function add(a: number, b: number) {
  return a + b;
}
